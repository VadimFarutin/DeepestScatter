    optix::TextureSampler getPhaseSampler(optix::Context context, const float* phase, size_t cnt)
    {
        optix::Buffer buffer = context->createBuffer(RT_BUFFER_INPUT, RT_FORMAT_FLOAT);
        buffer->setSize(cnt);

        {
            float_t* values = (float_t*)buffer->map();
            memcpy(values, phase, cnt * sizeof(float));

            float average = 0;
            for (size_t i = 0; i < cnt; i++)
            {
                average += values[i];
            }

            average /= cnt;

            for (size_t i = 0; i < cnt; i++)
            {
                values[i] /= average;
            }

            buffer->unmap();
        }

        optix::TextureSampler sampler = context->createTextureSampler();

        sampler->setWrapMode(0, RT_WRAP_REPEAT);

        sampler->setFilteringModes(
            RT_FILTER_LINEAR,
            RT_FILTER_LINEAR,
            RT_FILTER_NONE
        );

        sampler->setIndexingMode(RT_TEXTURE_INDEX_NORMALIZED_COORDINATES);
        sampler->setBuffer(buffer);

        return sampler;
    }

    optix::TextureSampler getIntegralSampler(optix::Context context, const float* phase, size_t cnt)
    {
        optix::Buffer buffer = context->createBuffer(RT_BUFFER_INPUT, RT_FORMAT_FLOAT);
        buffer->setSize(cnt);

        {
            float_t* values = (float_t*)buffer->map();
            memcpy(values, phase, cnt * sizeof(float));

            float sum = 0;
            for (size_t i = 0; i < cnt; i++)
            {
                sum += values[i];
            }

            float integral = 0;
            for (size_t i = 0; i < cnt; i++)
            {
                integral += values[i] / sum;
                values[i] = integral;
            }

            values[cnt - 1] = 1;

            buffer->unmap();
        }

        optix::TextureSampler sampler = context->createTextureSampler();

        sampler->setWrapMode(0, RT_WRAP_REPEAT);

        sampler->setFilteringModes(
            RT_FILTER_LINEAR,
            RT_FILTER_LINEAR,
            RT_FILTER_NONE
        );

        sampler->setIndexingMode(RT_TEXTURE_INDEX_NORMALIZED_COORDINATES);
        sampler->setBuffer(buffer);

        return sampler;
    }

